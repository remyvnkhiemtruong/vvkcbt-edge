export function isProductionUi(): boolean {
  const mode =
    typeof import.meta !== 'undefined'
      ? (import.meta as { env?: { VITE_UI_MODE?: string } }).env?.VITE_UI_MODE
      : 'production';
  return mode !== 'spec';
}

